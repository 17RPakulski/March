n1 = 5.5
n2 = 10.5

def averageOfTwo(a, b):
    average = (a + b) / 2
    return average
    
def remainderOfDivision(a,b):
    remainder = a % b
    return remainder

def power(a,b):
    power = a**b
    return power

print('average =', averageOfTwo(n1,n2),'   remainder =', remainderOfDivision(n1,n2),'   power =', power(n1,n2))
    
    